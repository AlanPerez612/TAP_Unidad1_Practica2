    int error=0;
    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        error=0;
        if(txtNombres.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Usted debe rellenar los nombres.");
            error++;
        }
        if(txtApellidos.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Usted debe rellenar los apellidos.");
            error++;
        }
        if(txtEdad.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Usted debe proporcionar una edad.");
            error++;
        }
        if(txtSobre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Proporciona informaci√≥n sobre ti.");
            error++;
        }
        if(txtGrado.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Complete el grado de estudios.");
            error++;
        }
        if(txtApellidos.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Debe rellenar los apellidos.");
            error++;
        }
        if(txtEstado.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Debe proporcionar un estado de estudios.");
            error++;
        }
        if(txtEscuela.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Debe proporcionar el nombre de la instituci√≥n.");
            error++;
        }
        if(error==0)JOptionPane.showMessageDialog(this, "Los campos han sido llenados correctamente.");
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(error==0){
            JOptionPane.showMessageDialog(this,"Informaci√≥n enviada correctamente.");
        }else{
            JOptionPane.showMessageDialog(this,"Primero debe verificar la informaci√≥n.");
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

